n=int(input("Enter the Marks of the Student:"))

if (n>90):
    print("Grade-S")

elif (n>80):
    print("Grade-A")

elif (n>70):
    print("Grade-B")

elif (n>60):
    print("Grade-C")

elif (n>50):
    print("Grade-D")

elif (n>40):
    print("Grade-E")

else:
    print("F")
